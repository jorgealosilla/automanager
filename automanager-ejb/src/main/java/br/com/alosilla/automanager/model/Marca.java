package br.com.alosilla.automanager.model;

public class Marca {
    private Long codigo;
    private String nome;
    
    public Marca(){
        
    }

    public Long getCodigo() {
        return codigo;
    }

    public void setCodigo(Long codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
